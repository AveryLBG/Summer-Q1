def Add(addend1, addend2):
    return addend1 + addend2 
def Sub(minuend, subtrahend):
    return minuend - subtrahend
def Mul(factor1, factor2):
    return factor1 * factor2
def Div(dividend, divisor):
    return dividend / divisor

Sum = Add(2,3)
Dif = Sub(10,2)
Pro = Mul(10,7)
Quo = Div(100,5)

print(Sum)
print(Dif)
print(Pro)
print(Quo)